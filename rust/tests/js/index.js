// Conventional if-elseif-else
if (x < 5) {
  console.log('x is less than 5');
} else if (x >= 5 && x < 10) {
  console.log('x is between 5 and 10');
} else {
  console.log('x is 10 or greater');
}

// Conventional for loop
for (let i = 0; i < 5; i++) {
  let a = i + 10;
  console.log('For loop iteration:', a, i);
}
// Conventional while loop
let count = 0;
while (count < 5) {
  console.log('While loop count:', count);
  count++;
}
